         public void populateData(){
                 DefaultTableModel manageCleanersModel = (DefaultTableModel) manageCollCentertbl.getModel();
                 manageCleanersModel.setRowCount(0);
                 int currentFoodbank=0;
                 
                 for(CollectionCenter foodbank:cityNetwork.getFoodBankDirectory().getFoodBanks()){
                     currentFoodbank++;
                     Object[] row = new Object[7];
                     row[0]=currentFoodbank;
                     row[1]=foodbank;
                     row[2]=foodbank.getLocation();
                    
                     manageCleanersModel.addRow(row);
                 }
     }

