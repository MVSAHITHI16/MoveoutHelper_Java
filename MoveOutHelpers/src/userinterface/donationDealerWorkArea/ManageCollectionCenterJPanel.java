    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        // TODO add your handling code here:
         int selectedRow = manageCollCentertbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        }else {
        CollectionCenter foodbank=(CollectionCenter) manageCollCentertbl.getValueAt(selectedRow, 1);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("Update Food Bank", new UpdateCollectionCenterJPanel(userProcessContainer, ecoSystem, foodbank, userAccount));
        cardLayout.next(userProcessContainer);
        }
         
    }//GEN-LAST:event_updatebuttonActionPerformed

    private void deletebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebuttonActionPerformed
        // TODO add your handling code here:
         int selectedRow=manageCollCentertbl.getSelectedRow();
         if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Food Bank to delete");
            return;
        }else{
             CollectionCenter foodbank=(CollectionCenter) manageCollCentertbl.getValueAt(selectedRow, 1);
             cityNetwork.getFoodBankDirectory().getFoodBanks().remove(foodbank);
             JOptionPane.showMessageDialog(null, "Successfully deleted Food Bank ");
      
            populateData();
            
            
         }
    }//GEN-LAST:event_deletebuttonActionPerformed


