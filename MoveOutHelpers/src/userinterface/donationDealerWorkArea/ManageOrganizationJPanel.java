    private void addManagerbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addManagerbuttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageOrgtable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Restaurant to add Managers");
            return;
        } else {
            DefaultTableModel organizationsModel = (DefaultTableModel) manageOrgtable.getModel();
            Organization selectedOrganization = (Organization) organizationsModel.getValueAt(selectedRow, 1);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("SysAdminCreateManagerPanel", new CreateOrgManagerJPanel(userProcessContainer, ecoSystem, cityNetwork, selectedOrganization, userLogged));
            cardLayout.next(userProcessContainer);

        }

    }//GEN-LAST:event_addManagerbuttonActionPerformed

    private void deleteOrgbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrgbuttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageOrgtable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            Organization org = (Organization) manageOrgtable.getValueAt(selectedRow, 1);
            
            cityNetwork.getOrganizationDirectory().getOrganizations().remove(org);
           
            JOptionPane.showMessageDialog(null, "Organization deleted Successfully! ");
            populateData();
        }
    }//GEN-LAST:event_deleteOrgbuttonActionPerformed

    private void managerOrgbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_managerOrgbuttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = manageOrgtable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            Organization org = (Organization) manageOrgtable.getValueAt(selectedRow, 1);
            DefaultTableModel managersModel = (DefaultTableModel) managerOrgtable.getModel();
            managersModel.setRowCount(0);
            int employeeCount=0;
            for(OrganizationManager orgManager:org.getOrgManagerDirectory().getOrgManagers()) {
                employeeCount++;
                Object[] row = new Object[7];
                row[0] = employeeCount;
                row[1] = orgManager;
                row[2] = orgManager.getPhoneNo();
                row[3] = orgManager.getEmail();
                row[4] = orgManager.getAddress();
                row[5] = orgManager.getUserName();
                row[6] = orgManager.getPassword();
                managersModel.addRow(row);
            }
        }
    }//GEN-LAST:event_managerOrgbuttonActionPerformed

    private void deleteManagerbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteManagerbuttonActionPerformed
        // TODO add your handling code here:
        int selectedManager = managerOrgtable.getSelectedRow();
        if(selectedManager < 0) {
            JOptionPane.showMessageDialog(this, "Please select a manager to delete");
            return;
        }
        DefaultTableModel managerModel = (DefaultTableModel) managerOrgtable.getModel();
        OrganizationManager orgManager = (OrganizationManager) managerOrgtable.getValueAt(selectedManager, 1);
        
        int selectedRow = manageOrgtable.getSelectedRow();
        DefaultTableModel orgManagerModel = (DefaultTableModel) manageOrgtable.getModel();
        Organization org = (Organization) orgManagerModel.getValueAt(selectedRow, 1);
    
        org.getOrgManagerDirectory().getOrgManagers().remove(orgManager);
        JOptionPane.showMessageDialog(this, "Successfully deleted Manager");
      
        populateData();
    
        
    }//GEN-LAST:event_deleteManagerbuttonActionPerformed

