    public void populateData(){
        DefaultTableModel model = (DefaultTableModel) manageOrgtable.getModel();
        model.setRowCount(0);
        int count=0;
        for(Organization org:cityNetwork.getOrganizationDirectory().getOrganizations()){
            count++;
            Object[] row = new Object[7];
            row[0]=count;
            row[1]=org;
            row[2]=org.getOrganizationType();
            row[3]=org.getAddress();
            row[4]=org.getPhoneNo();
            row[5]=org.getEmail();
            row[6]=org.getCreatedBy();
            
            model.addRow(row);
        }
        DefaultTableModel managersModel = (DefaultTableModel) managerOrgtable.getModel();
        managersModel.setRowCount(0);

    }

