     private boolean isDataEnteredValid() {
        if(ValidateInputs.isNameValid(nametext.getText()) && ValidateInputs.isUsernameValid(usrnametext.getText()) && 
                ValidateInputs.isEmailValid(emailIdText.getText()) && ValidateInputs.isPhoneNumberValid(phoneNumText.getText())
                && ValidateInputs.isPasswordValid(psswordText.getText()) && ValidateInputs.isAddressValid(addrssText.getText()))  {
         return true; 
        }
        return false;
    }

