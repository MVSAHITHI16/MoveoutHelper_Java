    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        if(isDataEnteredValid()) {
            
            if(!serviceAgent.getUserName().equals(usrnametext.getText()) && UserNameHelper.isUserNameAlreadyExisted(ecoSystem, usrnametext.getText())) {
                JOptionPane.showMessageDialog(this, "UserName already Exists in the Ecosystem.");
                return;
            }
            if(!serviceAgent.getEmail().equals(emailIdText.getText()) && EmailHelper.isEmailAlreadyExisted(ecoSystem, emailIdText.getText())) {
                JOptionPane.showMessageDialog(this, "Email already Exists in the Ecosystem.");
                return;
            }
            if(!serviceAgent.getPhoneNo().equals(phoneNumText.getText()) && PhoneNoHelper.isPhoneNoAlreadyExisted(ecoSystem, phoneNumText.getText())) {
                JOptionPane.showMessageDialog(this, "PhoneNo already Exists in the Ecosystem.");
                return;
            }
            
            serviceAgent.setName(nametext.getText());
            serviceAgent.setPhoneNo(phoneNumText.getText());
            serviceAgent.setAddress(addrssText.getText());
            serviceAgent.setEmail(emailIdText.getText());
            serviceAgent.setPassword(psswordText.getText());
            serviceAgent.setUserName(usrnametext.getText());
            serviceAgent.setModifiedBy(userAccount.getName());
            serviceAgent.setLastUpdatedDate(new Date());

            JOptionPane.showMessageDialog(this, "Successfully Updated ");
        }else {
            JOptionPane.showMessageDialog(this, "Error! Please enter valid values");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void uploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File("/"));
        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Jpeg format", "jpeg"));
        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Jpg format", "jpg"));
        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Png format", "png"));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            imagePath = selectedFile.getAbsolutePath();
            setPhoto(imagePath);
        }
    }//GEN-LAST:event_uploadButtonActionPerformed

