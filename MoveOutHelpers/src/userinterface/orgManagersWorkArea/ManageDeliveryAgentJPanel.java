    public void populateDeliveryVolunteers() {
        
        DefaultTableModel model = (DefaultTableModel) DeliveryAgentListLbl.getModel();
        model.setRowCount(0);
        int count = 0;
        for (DeliveryAgent deliveryAgent : org.getDeliveryVolunteerDirectory().getDeliveryVolunteers()) {

            count++;
            Object[] row = new Object[8];
            row[0] = count;
            row[1] = deliveryAgent;
            row[2] = deliveryAgent.getUserName();
            row[3] = deliveryAgent.getPassword();
            row[4] = deliveryAgent.getAddress();
            row[5] = deliveryAgent.getPhoneNo();
            row[6] = deliveryAgent.getEmail();
            row[7] = deliveryAgent.getCreatedBy();

            model.addRow(row);
        }
        deliveryAgentCountLbl.setText(String.valueOf(org.getDeliveryVolunteerDirectory().getDeliveryVolunteers().size()));
    }
    
