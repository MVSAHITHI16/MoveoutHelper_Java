    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = DeliveryAgentListLbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            DeliveryAgent deliveryAgent = (DeliveryAgent) DeliveryAgentListLbl.getValueAt(selectedRow, 1);
            org.getDeliveryVolunteerDirectory().getDeliveryVolunteers().remove(deliveryAgent);
            JOptionPane.showMessageDialog(null, "Delivery Volunteer deleted successfully");
            logger.log(Level.INFO, "Delivery Volunteer Profile deleted");
            populateDeliveryVolunteers();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

