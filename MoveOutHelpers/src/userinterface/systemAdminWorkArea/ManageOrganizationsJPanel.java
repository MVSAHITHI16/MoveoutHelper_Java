    public void populateOrganizations() {
        
        DefaultTableModel model = (DefaultTableModel) tblOrgList.getModel();
        model.setRowCount(0);
        int count=0;
        for(Organization org:cityNetwork.getOrganizationDirectory().getOrganizations()) {
            
            count++;
            Object[] row = new Object[7];
            row[0]=count;
            row[1]=org;
            row[2]=org.getOrganizationType().name();
            row[3]=org.getAddress();
            row[4]=org.getPhoneNo();
            row[5]=org.getEmail();
            row[6]=org.getCreatedBy();
                    
            model.addRow(row);
        }
        orgCountLbl.setText(String.valueOf(cityNetwork.getOrganizationDirectory().getOrganizations().size()));
        DefaultTableModel managersModel = (DefaultTableModel) tblOrgManagersJtable.getModel();
        managersModel.setRowCount(0);
    }
    
