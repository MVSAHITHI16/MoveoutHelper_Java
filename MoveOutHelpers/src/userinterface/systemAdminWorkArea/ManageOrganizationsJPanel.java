    private void btnManageOrgManagersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageOrgManagersActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrgList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            Organization selectedOrganization = (Organization) tblOrgList.getValueAt(selectedRow, 1);
            DefaultTableModel managersModel = (DefaultTableModel) tblOrgManagersJtable.getModel();
            managersModel.setRowCount(0);
            int employeeCount=0;
            for(OrganizationManager orgManager:selectedOrganization.getOrgManagerDirectory().getOrgManagers()) {
                employeeCount++;
                Object[] row = new Object[7];
                row[0] = employeeCount;
                row[1] = orgManager;
                row[2] = orgManager.getUserName();
                row[3] = orgManager.getPassword();
                row[4] = orgManager.getPhoneNo();
                row[5] = orgManager.getEmail();
                row[6] = orgManager.getAddress();

                managersModel.addRow(row);
            }
            orgManagerCountLbl.setText(String.valueOf(selectedOrganization.getOrgManagerDirectory().getOrgManagers().size()));
        }
    }//GEN-LAST:event_btnManageOrgManagersActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrgList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            Organization selectedOrganization = (Organization) tblOrgList.getValueAt(selectedRow, 1);
            cityNetwork.getOrganizationDirectory().getOrganizations().remove(selectedOrganization);
            JOptionPane.showMessageDialog(null, "Organization deleted successfully");

            populateOrganizations();
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

