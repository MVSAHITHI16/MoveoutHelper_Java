    private void deleteMangersBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMangersBtnActionPerformed
        // TODO add your handling code here:
        int selectedManager = tblOrgManagersJtable.getSelectedRow();
        if(selectedManager < 0) {
            JOptionPane.showMessageDialog(this, "Please select a manager to delete");
            return;
        }
        DefaultTableModel organizationManagerModel = (DefaultTableModel) tblOrgManagersJtable.getModel();
        OrganizationManager orgManager = (OrganizationManager) organizationManagerModel.getValueAt(selectedManager, 1);

        int selectedRow = tblOrgList.getSelectedRow();
        DefaultTableModel organizationsModel = (DefaultTableModel) tblOrgList.getModel();
        Organization selectedOrganization = (Organization) organizationsModel.getValueAt(selectedRow, 1);
        selectedOrganization.getOrgManagerDirectory().getOrgManagers().remove(orgManager);
        JOptionPane.showMessageDialog(this, "Successfully deleted Manager");
        populateOrganizations();
    }//GEN-LAST:event_deleteMangersBtnActionPerformed

    
