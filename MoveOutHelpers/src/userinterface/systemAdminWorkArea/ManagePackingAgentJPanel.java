    private void packingAgentDeletejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_packingAgentDeletejButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPackingAgentList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            PackingAgent selectedPackingAgent = (PackingAgent) tblPackingAgentList.getValueAt(selectedRow, 1);
            ecoSystem.getPackingAgentsDirectory().getPackingAgents().remove(selectedPackingAgent);
            JOptionPane.showMessageDialog(null, "Packing Agent deleted successfully");
            populatePackingAgents();
        }
    }//GEN-LAST:event_packingAgentDeletejButtonActionPerformed

    private void updatePackingAgentjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatePackingAgentjButtonActionPerformed
        int selectedRow = tblPackingAgentList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            PackingAgent packingAgent = (PackingAgent) tblPackingAgentList.getValueAt(selectedRow, 1);
            UpdatePackingAgentJPanel updatePackingAgentJPanel = new UpdatePackingAgentJPanel(userProcessJpanel, ecoSystem, packingAgent, userLogged);
            userProcessJpanel.add("UpdatePackingAgentJPanel", updatePackingAgentJPanel);
            CardLayout layout = (CardLayout) userProcessJpanel.getLayout();
            layout.next(userProcessJpanel);
        }
    }//GEN-LAST:event_updatePackingAgentjButtonActionPerformed

