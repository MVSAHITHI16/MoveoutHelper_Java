    public void populatePackingAgents() {
        DefaultTableModel model = (DefaultTableModel) tblPackingAgentList.getModel();
        model.setRowCount(0);
        int count = 0;
        for (PackingAgent packingAgent : ecoSystem.getPackingAgentsDirectory().getPackingAgents()) {

            count++;
            Object[] row = new Object[6];
            row[0] = count;
            row[1] = packingAgent;
            row[2] = packingAgent.getAddress();
            row[3] = packingAgent.getPhoneNo();
            row[4] = packingAgent.getEmail();
            row[5] = packingAgent.getCreatedBy();
            model.addRow(row);
        }
        packingAgentCountjLabel.setText(String.valueOf(ecoSystem.getPackingAgentsDirectory().getPackingAgents().size()));
    }

