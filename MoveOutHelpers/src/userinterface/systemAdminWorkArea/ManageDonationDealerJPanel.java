    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbldonationDonarList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            DonationDealer cityOfficial = (DonationDealer) tbldonationDonarList.getValueAt(selectedRow, 1);
            cityNetwork.getCityOfficialsDirectory().getCityOfficials().remove(cityOfficial);
            JOptionPane.showMessageDialog(null, "Donation Dealer deleted successfully");

            populateDonationDealers();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


