    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbldonationDonarList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        } else {
            DonationDealer cityOfficial = (DonationDealer) tbldonationDonarList.getValueAt(selectedRow, 1);
            UpdateDonationDealerJPanel updateDonationDealerJPanel = new UpdateDonationDealerJPanel(userProcessJpanel, ecoSystem, cityOfficial, userLogged);
            userProcessJpanel.add("UpdateDonationDealerPanel", updateDonationDealerJPanel);
            CardLayout layout = (CardLayout) userProcessJpanel.getLayout();
            layout.next(userProcessJpanel);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

