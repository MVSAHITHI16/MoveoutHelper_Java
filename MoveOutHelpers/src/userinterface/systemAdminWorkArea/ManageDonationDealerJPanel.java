    public void populateDonationDealers() {

        DefaultTableModel model = (DefaultTableModel) tbldonationDonarList.getModel();
        model.setRowCount(0);
        int count = 0;
        for (DonationDealer donationDealer : cityNetwork.getCityOfficialsDirectory().getCityOfficials()) {

            count++;
            Object[] row = new Object[8];
            row[0] = count;
            row[1] = donationDealer;
            row[2] = donationDealer.getUserName();
            row[3] = donationDealer.getPassword();
            row[4] = donationDealer.getAddress();
            row[5] = donationDealer.getPhoneNo();
            row[6] = donationDealer.getEmail();
            row[7] = donationDealer.getCreatedBy();

            model.addRow(row);
        }
        dealerdonarCountLbl.setText(String.valueOf(cityNetwork.getCityOfficialsDirectory().getCityOfficials().size()));
    }

