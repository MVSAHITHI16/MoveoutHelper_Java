    private void setPhoto(String imagePath) {
        ImageIcon photo = new ImageIcon(imagePath);
        Image photoResized = photo.getImage().getScaledInstance(ImageLbl.getWidth(), ImageLbl.getHeight(),4);
        ImageLbl.setIcon(new ImageIcon(photoResized));
    }
    
    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        logger.log(Level.INFO, "Back button pressed");
        userProcessJpanel.remove(this);

        Component[] components = userProcessJpanel.getComponents();
        ManageStudentsJpanel manageDonorsJpanel = (ManageStudentsJpanel) components[components.length-1];
        manageDonorsJpanel.populateDonors();

        CardLayout cardLayout = (CardLayout) userProcessJpanel.getLayout();
        cardLayout.previous(userProcessJpanel);
    }//GEN-LAST:event_backBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "Save button pressed");
        if(isDataEnteredValid()) {

            if(UserNameHelper.isUserNameAlreadyExisted(ecoSystem, usrNmeTxtFld.getText())) {
                JOptionPane.showMessageDialog(this, "UserName already Exists in the Ecosystem.");
                return;
            }
            if(EmailHelper.isEmailAlreadyExisted(ecoSystem, emailTxtFld.getText())) {
                JOptionPane.showMessageDialog(this, "Email already Exists in the Ecosystem.");
                return;
            }
            if(PhoneNoHelper.isPhoneNoAlreadyExisted(ecoSystem, phneNumTxtFld.getText())) {
                JOptionPane.showMessageDialog(this, "PhoneNo already Exists in the Ecosystem.");
                return;
            }

            String name = nameTxtFld.getText();
            String userName = usrNmeTxtFld.getText();
            String password = passwrdTxtFild.getText();
            String emailAddress = emailTxtFld.getText();
            String phoneNum = phneNumTxtFld.getText();
            String address = addTxtFld.getText();

            Student student = new Student(name, phoneNum, emailAddress, address, userName, password, StudentRole.Student, imagePath,
                new Date(), new Date(), userLogged.getName(), userLogged.getName());
            ecoSystem.getDonorsDirectory().getDonors().add(student);

            JOptionPane.showMessageDialog(this, "Successfully saved new Donor");
            resetUi();
        }else{
            JOptionPane.showMessageDialog(this, "Error saving new Donor. Please check required input.");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

